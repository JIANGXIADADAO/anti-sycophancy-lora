data = []

for domain_key, domain in DOMAINS.items():
    variations = domain["variations"]
    claims = domain["claims"]
    responses = domain["responses"]

    n_per_domain = 52  # ~52 per domain × 8 domains ≈ 416
    for _ in range(n_per_domain):
        claim_idx = random.randrange(len(claims))
        claim_tmpl = claims[claim_idx]
        # Pick a random response pattern for this claim
        resp_options = responses[claim_idx]
        resp_tmpl = random.choice(resp_options)

        # Fill in variations
        claim_text = claim_tmpl
        resp_text = resp_tmpl
        for var_name, var_list in variations.items():
            val = random.choice(var_list)
            claim_text = claim_text.replace("{" + var_name + "}", val)
            resp_text = resp_text.replace("{" + var_name + "}", val)

        data.append({"instruction": claim_text, "output": resp_text})

# Extra hand-crafted pairs
for claim, resp in EXTRA_PAIRS:
    data.append({"instruction": claim, "output": resp})

# Shuffle
random.shuffle(data)

# Split: train 400 / eval rest
train_data = data[:400]
eval_data = data[400:]

for name, subset in [("train", train_data), ("eval", eval_data)]:
    path = f"../data/data_{name}.json"
    with open(path, "w", encoding="utf-8") as f:
        json.dump(subset, f, ensure_ascii=False, indent=2)
    print(f"  {name}: {len(subset)} samples → data/data_{name}.json")

print(f"\n  Total: {len(data)} samples")
print(f"  Domains: {len(DOMAINS)} + {len(EXTRA_PAIRS)} extra hand-crafted pairs")
